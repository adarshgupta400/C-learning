// working with numbers part 2     MATH FUNCTIONS

#include <iostream>
#include <cmath>  // for math functions use CMATH

using namespace std;
int main() {
     cout << pow(2,5)<<endl ;  //2 raise to power 5
     cout << sqrt(36) <<endl;
     cout << round(6.25) <<endl;
     cout << sqrt(36.6) <<endl;
     cout << ceil(3.1) <<endl;
     cout << floor(3.9) <<endl;
     cout << fmax(3,5) <<endl;  //bigger of 2 int
     cout << fmin(3,5) <<endl;

     return 0;
}

